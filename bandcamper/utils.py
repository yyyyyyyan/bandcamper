def get_random_user_agent():
    """Random User-Agent from list of 85 (allegedly) most used.

    Returns
    -------
    str
        Valid User-Agent.

    Notes
    -----
    The User-Agent list was taken from the article 'Most Common User Agents' [1]_, from 'Tech Blog (wh)'
    (techblog.willshouse.com)

    References
    ----------
    .. [1] "Most Common User Agents - Tech Blog (wh)", Tech Blog (wh), 2020. [Online].
        Available: https://archive.vn/xrdym. [Accessed: 12- Oct- 2020].
    """
    return random_choice(USER_AGENTS)


def get_random_filename_template():
    return uuid4().hex[:16] + ".{ext}"


def get_download_file_extension(file_type):
    """File extension of given extension.

    Get file extension, including the leading dot (`"."`), associated with a given MIME type.

    Parameters
    ----------
    file_type : str
        MIME file type.

    Returns
    -------
    str
        File extension associated with given `file_type`, including the leading dot (`"."`).

    Notes
    -----
    The function tries to get the extension from the `MIME_TYPES` constant, created based on Bandcamp's current
    behavior. If no extension is found, the function uses `mimetypes.guess_extension` to try to guess the correct
    extension. If no extension is guessed, an empty string (`""`) is returned.

    Examples
    --------
    >>> get_download_file_extension("audio/alac")
    '.m4a'
    >>> get_download_file_extension("audio/x-aiff")
    '.aiff'
    >>> get_download_file_extension("audio/x-flac")
    '.flac'
    >>> get_download_file_extension("fake/mime")
    ''
    """
    return MIME_TYPES.get(file_type, guess_extension(file_type, False)) or ""


def humanize_bytes(num):
    """Human-readable file size from amount of bytes.

    Parameters
    ----------
    num : int or float
        Total number of bytes

    Returns
    -------
    str
        Human-readable amount of bytes.

    Notes
    -----
    The amount of bytes if formatted using the IEC standard, being yobibyte (:math:`1024^8` bytes) the biggest multiple
    of bytes.

    Examples
    --------
    >>> humanize_bytes(1000)
    '1000.0B'
    >>> humanize_bytes(1000000)
    '976.6KiB'
    >>> humanize_bytes(1000000000)
    '953.7MiB'
    >>> humanize_bytes(1000000000000)
    '931.3GiB'
    >>> humanize_bytes(1000000000000000)
    '909.5TiB'
    >>> humanize_bytes(1000000000000000000)
    '888.2PiB'
    >>> humanize_bytes(1000000000000000000000)
    '867.4EiB'
    >>> humanize_bytes(1000000000000000000000000)
    '847.0ZiB'
    >>> humanize_bytes(1000000000000000000000000000)
    '827.2YiB'
    >>> humanize_bytes(1000000000000000000000000000000)
    '827180.6YiB'
    """
    for unit in ["", "Ki", "Mi", "Gi", "Ti", "Pi", "Ei", "Zi"]:
        if abs(num) < 1024.0:
            return f"{num:3.1f}" + unit + "B"
        num /= 1024.0
    return f"{num:.1f}YiB"
